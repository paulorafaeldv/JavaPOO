package metodos;

public class CalcularMetodos {
	int somar (int a, int b) {
		return a + b;
	}
	int somar (int a, int b, int c) {
		return a + b + c;
	}
	int subtrair (int a, int b) {
		return a - b;
	}
	int mult (int a, int b) {
		return a * b;
	}
	double dividir (double a, double b) {
		return a / b;
	}
}
